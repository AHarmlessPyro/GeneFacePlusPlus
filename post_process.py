def get_mask(image: np.ndarray):
    gray_image = cv2.cvtColor(image, cv2.COLOR_BGR2GRAY)
    _, threshold_mask = cv2.threshold(gray_image, 1, 255, cv2.THRESH_BINARY)
    return threshold_mask


def get_bb(image: np.ndarray):
    threshold_mask = get_mask(image)

    threshold_points = cv2.findNonZero(threshold_mask)
    bounding = cv2.boundingRect(threshold_points)

    return bounding


